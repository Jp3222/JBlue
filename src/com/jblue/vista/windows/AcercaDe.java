    private void launch(String uri) {
        try {
            if (!Desktop.isDesktopSupported()) {
                return;
            }
            Desktop.getDesktop().browse(URI.create(uri));
        } catch (IOException ex) {
            Logger.getLogger(AcercaDe.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

