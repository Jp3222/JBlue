    String[] _getInfo() {
        String toma = null, calle = null, estado = null, titular = "-1", fch = null;
        calle = EnvUsuario.getCalle(jcbUsuariosCalle.getItemAt(jcbUsuariosCalle.getSelectedIndex())).getId();
        System.out.println(calle);
        toma = EnvUsuario.getTipo_Toma(jcbUsuariosToma.getItemAt(jcbUsuariosToma.getSelectedIndex())).getId();
        fch = this.fecha.getFechaActualString();
        estado = jcbUsuariosEst.getSelectedIndex() == 1 ? "1" : "-1";
        if (jcbUsuariosRoll.getSelectedIndex() == 2) {
            titular = EnvUsuario.getUsuario(jcbUsuariosTitular.getItemAt(jcbUsuariosTitular.getSelectedIndex())).getId();
        }
        return new String[]{
            jtfUsuariosNom.getText(),
            jtfUsuariosAp.getText(),
            jtfUsuariosAm.getText(),
            calle,
            toma,
            fch,
            estado,
            titular
        };
    }

