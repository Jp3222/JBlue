    @Override
    public void getDataView() {
        ProcessWrapperDTO pw = getProcessWrapper();
        if (pw == null) {
            EmployeeDTO emp = SystemSession.getInstancia().getCurrentEmployee();
            pw = new ProcessWrapperDTO(emp);
            setProcessWrapper(pw);
        }
        if (!isValuesOK()) {
            return;
        }
        UserDTO o = getValues(false);
        pw.setUser(o);
    }

