    public String getURL() {
        return String.format("jdbc:%s://%s:%s/%s",
                jcbMotor.getItemAt(jcbMotor.getSelectedIndex()),
                jtfHost.getText(),
                jtfPuerto.getText(),
                jtfBDNombre.getText()
        );

    }

