    private void jlUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlUsuariosMouseClicked
        int click = evt.getClickCount();
        int select = jlUsuarios.getSelectedIndex();
        OUsuarios usuarioSeleccionado = LISTA_AUX.get(select);
        switch (click) {
            case 1:
                jtfBuscarUsuarioEnLista.setText(usuarioSeleccionado.getNombreStr());
                jlUsuarios.clearSelection();
                break;
            case 2:
                jtfBuscarUsuarioEnLista.setText(usuarioSeleccionado.getNombreStr());
                jlUsuarios.clearSelection();
                jtfUsuariosNom.setText(usuarioSeleccionado.getNombre());
                jtfUsuariosAp.setText(usuarioSeleccionado.getAp());
                jtfUsuariosAm.setText(usuarioSeleccionado.getAm());
                jcbUsuariosCalle.setSelectedItem(
                        EnvUsuario.getCalle(usuarioSeleccionado.getCalle()).getCalleStr()
                );
                jcbUsuariosToma.setSelectedItem(
                        EnvUsuario.getTipo_Toma(usuarioSeleccionado.getToma()).getTipo()
                );
                jcbUsuariosRoll.setSelectedIndex(usuarioSeleccionado.getTitutlar() == -1 ? 1 : 2);
                jcbUsuariosEst.setSelectedIndex(usuarioSeleccionado.getEstado() == 1 ? 1 : 2);
                if (usuarioSeleccionado.getTitutlar() != -1) {
                    jcbUsuariosTitular.setSelectedItem(
                            EnvUsuario.getUsuario(usuarioSeleccionado.getTitutlar() + "")
                                    .getNombreStr()
                    );
                }
                break;
        }
    }//GEN-LAST:event_jlUsuariosMouseClicked

    private void jtfBuscarUsuarioEnListaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscarUsuarioEnListaKeyReleased
        MODELO_LISTA.removeAllElements();
        LISTA_AUX.clear();
        String buscar = jtfBuscarUsuarioEnLista.getText();
        buscar = buscar.trim().replace(" ", "").toUpperCase();
        for (OUsuarios oUsuarios : lista_cache) {
            String nombre = oUsuarios.getNombreStr().trim().replace(" ", "").toUpperCase();
            if (nombre.contains(buscar)) {
                MODELO_LISTA.addElement(oUsuarios.getNombreStr());
                LISTA_AUX.add(oUsuarios);
            }
        }
    }//GEN-LAST:event_jtfBuscarUsuarioEnListaKeyReleased

    private void jtfBuscarUsuarioEnTablaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfBuscarUsuarioEnTablaKeyReleased
        this.buscando = true;
        String buscar = jtfBuscarUsuarioEnTabla.getText();
        buscar = buscar.trim().replace(" ", "").toUpperCase();
        _vaciarTabla();
        for (OUsuarios oUsuarios : lista_cache) {
            if (oUsuarios.getNombreStr().toUpperCase().replace(" ", "").contains(buscar)) {
                modelo_tabla_usuarios.addRow(oUsuarios.getInfoSinFK());
                LISTA_AUX.add(oUsuarios);
            }
        }
    }//GEN-LAST:event_jtfBuscarUsuarioEnTablaKeyReleased

