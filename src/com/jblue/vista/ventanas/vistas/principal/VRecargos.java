    private void buscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarKeyReleased
        buscando = true;

        lista_aux.clear();
        modelo.removeAllElements();
        //

        String entrada = buscar.getText();
        entrada = _limpiar(entrada);

        for (OUsuarios i : cache) {
            String aux = _limpiar(i.getStringR());
            if (!aux.contains(entrada)) {
                continue;
            }
            modelo.addElement(i.getStringR());
            lista_aux.add(i);
        }
    }//GEN-LAST:event_buscarKeyReleased

    String _limpiar(String txt) {
        return txt.trim().replace(" ", "").replace("_", "").toLowerCase();
    }

