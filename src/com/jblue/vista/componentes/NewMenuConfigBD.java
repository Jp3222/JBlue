    private void btn_probar_conexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_probar_conexionActionPerformed
        try {
            Conexion con = Conexion.getInstancia(getUsuario(), getContraseña(), getURL());
            boolean cerrado = con.getConexion().isClosed();

            String mensaje = "La conexion es: ";

            if (!cerrado) {
                mensaje += "valida";
            } else {
                mensaje += "no valida";
            }
            JOptionPane.showMessageDialog(this, mensaje);
            Conexion.ConexionNULL();

        } catch (SQLException ex) {
            Excp.imp(ex, getClass(), true, true);
        }
    }//GEN-LAST:event_btn_probar_conexionActionPerformed

    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }

